#!/usr/bin/env bash
set -ex
. $(dirname ${BASH_SOURCE[0]})/_init
process_args "$@"

# SDL3_image version
SDL3_IMAGE_VERSION=3.4.4
SDL3_IMAGE_URL="https://github.com/libsdl-org/SDL_image/releases/download/release-${SDL3_IMAGE_VERSION}/SDL3_image-${SDL3_IMAGE_VERSION}.tar.gz"
SDL3_IMAGE_SHA256=29751304a13d25ac513f24305fa25b06a6edd9607718c90129b8350d35fc5573

SDL3_IMAGE_PROJECT_DIR="SDL3_image-$SDL3_IMAGE_VERSION"
SDL3_IMAGE_FILENAME="$SDL3_IMAGE_PROJECT_DIR.tar.gz"

cd "$SOURCES_DIR"

if [[ ! -d "$SDL3_IMAGE_PROJECT_DIR" ]]
then
    get_file "$SDL3_IMAGE_URL" "$SDL3_IMAGE_FILENAME" "$SDL3_IMAGE_SHA256"
    tar xf "$SDL3_IMAGE_FILENAME"
fi

mkdir -p "$BUILD_DIR/$SDL3_IMAGE_PROJECT_DIR"
cd "$BUILD_DIR/$SDL3_IMAGE_PROJECT_DIR"

export CFLAGS='-O2'
export CXXFLAGS="$CFLAGS"

if [[ ! -d "$DIRNAME" ]]
then
    mkdir "$DIRNAME"
    cd "$DIRNAME"

    SDL3_DIR="$INSTALL_DIR/$DIRNAME"

    CMAKE_OPTS=(
        -DCMAKE_INSTALL_PREFIX="$INSTALL_DIR/$DIRNAME"
        -DCMAKE_BUILD_TYPE=Release
        -DBUILD_SHARED_LIBS=OFF
        -DSDL3IMAGE_SAMPLES=OFF
        -DSDL3IMAGE_INSTALL=ON
        -DSDL3IMAGE_DEPS_SHARED=OFF
        -DSDL3IMAGE_VENDORED=OFF
        -DSDL3IMAGE_AVIF=OFF
        -DSDL3IMAGE_BMP=ON
        -DSDL3IMAGE_GIF=OFF
        -DSDL3IMAGE_JPG=OFF
        -DSDL3IMAGE_JXL=OFF
        -DSDL3IMAGE_LBM=OFF
        -DSDL3IMAGE_PCX=OFF
        -DSDL3IMAGE_PNG=ON
        -DSDL3IMAGE_PNM=OFF
        -DSDL3IMAGE_QOI=OFF
        -DSDL3IMAGE_SVG=OFF
        -DSDL3IMAGE_TGA=OFF
        -DSDL3IMAGE_TIF=OFF
        -DSDL3IMAGE_WEBP=OFF
        -DSDL3IMAGE_XCF=OFF
        -DSDL3IMAGE_XPM=OFF
        -DSDL3IMAGE_XV=OFF
        -DSDL3IMAGE_BACKEND_STB=ON
        -DCMAKE_PREFIX_PATH="$SDL3_DIR"
    )

    if [[ "$BUILD_TYPE" == cross ]]
    then
        CMAKE_OPTS+=(
            -DCMAKE_SYSTEM_NAME=Windows
            -DCMAKE_C_COMPILER=${HOST_TRIPLET}-gcc
            -DCMAKE_CXX_COMPILER=${HOST_TRIPLET}-g++
            -DCMAKE_RC_COMPILER=${HOST_TRIPLET}-windres
            -DCMAKE_FIND_ROOT_PATH="$INSTALL_DIR/$DIRNAME"
            -DCMAKE_FIND_ROOT_PATH_MODE_PROGRAM=NEVER
            -DCMAKE_FIND_ROOT_PATH_MODE_LIBRARY=ONLY
            -DCMAKE_FIND_ROOT_PATH_MODE_INCLUDE=ONLY
        )
    fi

    cmake "$SOURCES_DIR/$SDL3_IMAGE_PROJECT_DIR" "${CMAKE_OPTS[@]}"
else
    cd "$DIRNAME"
fi

make -j
make install
